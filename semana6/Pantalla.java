    void jugar() {
        String letra = entradaLetra.getText().toUpperCase();
        entradaLetra.setText("");
        

        if (letra.length() != 1 || !Character.isLetter(letra.charAt(0))) {
            JOptionPane.showMessageDialog(this, "SOLO DEBE INGRESAR UNA LETRA VALIDA");
            return;
        }

        char letraIngresada = letra.charAt(0);
        boolean acierto = false;

        for (int i = 0; i < palabra.length(); i++) {
            if (palabra.charAt(i) == letraIngresada && oculta[i] == '_') {
                oculta[i] = letraIngresada;
                acierto = true;
            }
        }

        if (!acierto) {
            oportunidades--;
            JOptionPane.showMessageDialog(this, "La palabra no contiene esa letra. Te quedan " + oportunidades + " intentos.");
        }
        
        actualizarPalabraOculta();

        if (new String(oculta).equals(palabra)) {
            JOptionPane.showMessageDialog(this, "Â¡Felicidades! Adivinaste la palabra: " + palabra);
            iniciarJuego();
        } else if (oportunidades == 0) {
            JOptionPane.showMessageDialog(this, "Perdiste. La palabra era: " + palabra);
            iniciarJuego();
        }
    }

    void actualizarPalabraOculta() {
        String texto = "";
        for (int i = 0; i < palabra.length(); i++) {
            texto += oculta[i] + " ";
        }
        palabraOculta.setText(texto);
    }

    void iniciarJuego() {
        palabra = listaPalabras[random.nextInt(listaPalabras.length)];
        oculta = new char[palabra.length()];
        for (int i = 0; i < palabra.length(); i++) {
            oculta[i] = '_';
        }
        oportunidades = 5;
        actualizarPalabraOculta();
    }

    void cambiarPalabras() {
        for (int i = 0; i < listaPalabras.length; i++) {
            String nueva = JOptionPane.showInputDialog(this, "Palabra #" + (i + 1) + " actual: " + listaPalabras[i]);
            if (nueva != null) {
                listaPalabras[i] = nueva.toUpperCase();
            }
        }
        Listado.setVisible(false);
        jLabel4.setVisible(false);
        

        actualizarLista();   
        iniciarJuego();      
    }

    public void actualizarLista() {
        String[] textoLista = new String[listaPalabras.length];

        for (int i = 0; i < listaPalabras.length; i++) {
            textoLista[i] = (i + 1) + ". " + listaPalabras[i];
        }

        Listado.setListData(textoLista);
    }


