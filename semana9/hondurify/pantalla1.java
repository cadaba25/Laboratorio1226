    private void agregarCancionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agregarCancionActionPerformed
        // TODO add your handling code here:
        int cod = Integer.parseInt(codigo.getText());
        String nom = nombre.getText();
        double pre = Double.parseDouble(precio.getText());

        boolean exito = jtunes.addSong(cod, nom, pre, imagenSeleccionada);

        if (exito) {
            JOptionPane.showMessageDialog(null, "Cancion agregada correctamente.");
            Song nueva = jtunes.searchSong(cod);
            agregarCajaCancion(nueva);
            codigo.setText("");
            nombre.setText("");
            precio.setText("");
            

        } else {
            JOptionPane.showMessageDialog(null, "Codigo repetido");
        }
        
        
        


    }//GEN-LAST:event_agregarCancionActionPerformed

    private void archivoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_archivoMouseClicked
        // TODO add your handling code here:
        JFileChooser selector = new JFileChooser();

        int resultado = selector.showOpenDialog(null);

        if (resultado == JFileChooser.APPROVE_OPTION) {
            File archivoImg = selector.getSelectedFile();
            imagenSeleccionada = new ImageIcon(archivoImg.getAbsolutePath());

            Image imgEscalada = imagenSeleccionada.getImage().getScaledInstance(
                    archivo.getWidth(), archivo.getHeight(), Image.SCALE_SMOOTH
            );
            archivo.setIcon(new ImageIcon(imgEscalada));

        }

    }//GEN-LAST:event_archivoMouseClicked

