    public void agregarCajaCancion(Song s) {

        JPanel caja = new JPanel(new BorderLayout());

        caja.addMouseListener(new java.awt.event.MouseAdapter() {
            public void mouseClicked(java.awt.event.MouseEvent evt) {
                String[] opciones = {"0", "1", "2", "3", "4", "5"};
                String seleccion = (String) JOptionPane.showInputDialog(
                        null,
                        "Califica \"" + s.getNombre() + "\" con estrellas:",
                        "Rating",
                        JOptionPane.QUESTION_MESSAGE,
                        null,
                        opciones,
                        "5"
                );

                if (seleccion != null) {
                    int estrellas = Integer.parseInt(seleccion);
                    s.addStars(estrellas); 

                    actualizarPanelCanciones();
                }
            }
        });

        caja.setBorder(BorderFactory.createLineBorder(Color.GRAY));
        caja.setMaximumSize(new Dimension(600, 100));
        caja.setBackground(Color.WHITE);

        JLabel lblImagen = new JLabel();
        if (s.getImagenDisco() != null) {
            Image img = s.getImagenDisco().getImage().getScaledInstance(80, 80, Image.SCALE_SMOOTH);
            lblImagen.setIcon(new ImageIcon(img));
        } else {
            lblImagen.setText("sin imagen");
        }

        JPanel datos = new JPanel();
        datos.setLayout(new BoxLayout(datos, BoxLayout.Y_AXIS));
        datos.setBackground(Color.WHITE);
        datos.add(new JLabel("Codigo: " + s.getCodigo()));
        datos.add(new JLabel("Nombre: " + s.getNombre()));
        datos.add(new JLabel("Precio: Lps. " + s.getPrecio()));
        datos.add(new JLabel("Rating: " + String.format("%.1f", s.songRating()) + " ⭐"));

        JPanel estrellasVisuales = new JPanel();
        estrellasVisuales.setBackground(Color.WHITE);

        int estrellasEnteras = (int) Math.round(s.songRating());

        for (int i = 1; i <= 5; i++) {
            JLabel estrella = new JLabel(i <= estrellasEnteras ? "⭐" : "☆");
            estrellasVisuales.add(estrella);
        }

        datos.add(estrellasVisuales);

        caja.add(lblImagen, BorderLayout.WEST);
        caja.add(datos, BorderLayout.CENTER);

        panelCanciones.add(caja);
        panelCanciones.revalidate();
        panelCanciones.repaint();
        
    }

    public void actualizarPanelCanciones() {
        panelCanciones.removeAll();
        for (Song s : jtunes.getAllSongs()) {
            agregarCajaCancion(s);
        }
        panelCanciones.revalidate();
        panelCanciones.repaint();
    }

